public class TwoNumbers {
    public static void main(String[] args) {
        int a = 22;
        int b = 13;
        double d = ((double)a ) / b;
        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(d);
        System.out.println(a / b);
        System.out.println(a % b);
    }
}
